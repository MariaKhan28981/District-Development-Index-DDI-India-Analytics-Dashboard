# ---------------- LOAD ---------------- #

@st.cache_data
def load_data():

    ddi = pd.read_csv(
        "data/processed/district_development_index.csv"
    )

    ddi["State"]=ddi["State"].str.strip()
    ddi["District"]=ddi["District"].str.strip()
    ddi["Year"]=ddi["Year"].astype(int)

    return ddi



ddi = load_data()



@st.cache_data
def load_geojson():

    with open(
        "data/geojson/india_district.geojson",
        encoding="utf-8"
    ) as f:

        return json.load(f)



geojson=load_geojson()




