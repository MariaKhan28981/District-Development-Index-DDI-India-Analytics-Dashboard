# ---------------- FILTERS ---------------- #

st.header("🎛️ Select Analysis")


a,b,c=st.columns(3)


with a:

    state=st.selectbox(
        "State",
        sorted(ddi.State.unique())
    )


with b:

    years=sorted(
        ddi[
        ddi.State==state
        ].Year.unique()
    )


    year=st.slider(
        "Year",
        min(years),
        max(years),
        max(years)
    )


data=ddi[
    (ddi.State==state)
    &
    (ddi.Year==year)
]


with c:

    district=st.selectbox(
        "District",
        sorted(data.District.unique())
    )

# ---------------- RANKING ---------------- #

data=data.copy()


# State rank
state_rank = (
    ddi[
    (ddi.Year==year) &
    (ddi.State==state)
    ]
    .sort_values(
        "DDI",
        ascending=False
    )
    .reset_index(drop=True)
)

state_rank["Rank"]=state_rank.index+1


state_rank_value=int(
    state_rank[
    state_rank.District==district
    ].Rank.iloc[0]
)



# India rank
india_rank = (
    ddi[
    ddi.Year==year
    ]
    .sort_values(
        "DDI",
        ascending=False
    )
    .reset_index(drop=True)
)

india_rank["Rank"]=india_rank.index+1


india_rank_value=int(
    india_rank[
    india_rank.District==district
    ].Rank.iloc[0]
)

selected=data[
    data.District==district
].iloc[0]

ddi_value=selected.DDI

# percentile among all districts for selected year

percentile = (
    ddi[
        ddi.Year==year
    ]["DDI"]
    .rank(pct=True)
    .loc[
        ddi[
        (ddi.Year==year) &
        (ddi.District==district)
        ].index[0]
    ]
)


if percentile>=0.75:
    category="🟢 High Development"

elif percentile >=0.5:
    category="🟡 Medium Development"

else:
    category="🔴 Low Development"


