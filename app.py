# ---------------- MAP ---------------- #

st.header("🗺️ India District Development Map")



map_df=ddi[
    ddi.Year==year
].copy()


map_df["clean"]=(
    map_df.District
    .str.lower()
    .str.replace(" ","")
)


for f in geojson["features"]:

    name=f["properties"].get(
        "NAME_2",""
    )

    f["properties"]["clean"]=(
        name.lower()
        .replace(" ","")
    )




m=folium.Map(
    location=[22.5,80],
    zoom_start=4
)



folium.Choropleth(
    geo_data=geojson,
    data=map_df,
    columns=[
        "clean",
        "DDI"
    ],
    key_on="feature.properties.clean",
    fill_opacity=0.75,
    legend_name="District DDI"
).add_to(m)



st_folium(
    m,
    width=1200,
    height=600
)
